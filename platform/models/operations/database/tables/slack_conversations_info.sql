CREATE TABLE IF NOT EXISTS SLACK_CONVERSATIONS_INFO (
    ID INTEGER PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    CHANNEL_ID VARCHAR,
    CREATED TIMESTAMPTZ,
    CREATOR VARCHAR,
    IS_ORG_SHARED BOOLEAN,
    IS_IM BOOLEAN,
    TEAM_ID VARCHAR,
    UPDATED TIMESTAMPTZ,
    NAME VARCHAR,
    NAME_NORMALIZED VARCHAR,
    IS_CHANNEL BOOLEAN,
    IS_GROUP BOOLEAN,
    IS_MPIM BOOLEAN,
    IS_PRIVATE BOOLEAN,
    IS_ARCHIVED BOOLEAN,
    IS_GENERAL BOOLEAN,
    IS_SHARED BOOLEAN,
    IS_EXT_SHARED BOOLEAN,
    UNLINKED INTEGER,
    IS_PENDING_EXT_SHARED BOOLEAN,
    PENDING_SHARE TEXT[],
    PARENT_CONVERSATION VARCHAR,
    PURPOSE_VALUE TEXT,
    PURPOSE_CREATOR VARCHAR,
    PURPOSE_LAST_SET BIGINT,
    TOPIC_VALUE TEXT,
    TOPIC_CREATOR VARCHAR,
    TOPIC_LAST_SET BIGINT,
    SHARED_TEAM_IDS VARCHAR[],
    PENDING_CONNECTED_TEAM_IDS VARCHAR[],
    CANVAS_FILE_ID VARCHAR,
    CANVAS_IS_EMPTY BOOLEAN,
    CANVAS_QUIP_THREAD_ID VARCHAR,
    CANVAS_IS_MIGRATED BOOLEAN,
    PROPERTIES JSONB,
    PAYLOAD JSONB
);